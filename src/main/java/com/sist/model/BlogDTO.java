package com.sist.model;


public class BlogDTO {

	private int b_No;

	private String b_Name;

	private String b_Intro;

	private String b_Picture;

	public int getB_No() {
		return b_No;
	}

	public void setB_No(int b_No) {
		this.b_No = b_No;
	}

	public String getB_Name() {
		return b_Name;
	}

	public void setB_Name(String b_Name) {
		this.b_Name = b_Name;
	}

	public String getB_Intro() {
		return b_Intro;
	}

	public void setB_Intro(String b_Intro) {
		this.b_Intro = b_Intro;
	}

	public String getB_Picture() {
		return b_Picture;
	}

	public void setB_Picture(String b_Picture) {
		this.b_Picture = b_Picture;
	}

}
